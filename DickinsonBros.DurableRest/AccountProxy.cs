using RestSharp;
using System.Threading.Tasks;

namespace DickinsonBros.DurableRest
{
    public class AccountProxy
    {
        public AccountProxy()
        public async Task<object> Login()
        {
            var durableRestService = new DurableRestService(null);

            var client = new RestClient("");
            var request = new RestRequest();

            request.Resource = "";

            //Key Value Object -> Prams
            request.AddParameter("Username", "");
            request.AddParameter("Password", "");

            request.Method = Method.POST;
            request.Timeout = 30000;

            return await durableRestService.ExecuteAsync<object>(client, request, 3);
        }
